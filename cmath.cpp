#include <iostream>
#include <cmath>
using namespace std;

int main()
{

  cout << max(10, 232);
  cout << "\n"
       << min(10, 232);
  cout << "\n"
       << sqrt(64);
  cout << "\n"
       << round(32.49);
  cout << "\n"
       << ceil(32.32);
  cout << "\n"
       << floor(32.32);

  return 0;
}